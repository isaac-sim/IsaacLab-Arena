"""RoboGate benchmark report generator.

Produces JSON and human-readable text reports from evaluation results.
Compatible with the RoboGate core report format and Isaac Lab-Arena
metrics logging.

Output formats:
    - JSON: Machine-readable full results
    - Text: Human-readable summary for terminal/CI output
    - Isaac Lab-Arena: MetricsLogger-compatible dict
"""

from __future__ import annotations

import json
import time
from pathlib import Path
from typing import Any


def generate_json_report(
    results: dict[str, Any],
    output_path: str | Path,
) -> Path:
    """Write full evaluation results as JSON.

    Args:
        results: Results dict from VLAEvaluator or benchmark runner.
        output_path: File path for JSON output.

    Returns:
        Path to written file.
    """
    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    with open(output_path, "w") as f:
        json.dump(results, f, indent=2, default=str)

    return output_path


def generate_text_report(results: dict[str, Any]) -> str:
    """Generate a human-readable text report.

    Args:
        results: Results dict from evaluation.

    Returns:
        Formatted text string.
    """
    meta = results.get("metadata", {})
    summary = results.get("summary", {})
    metrics = results.get("metrics", {})
    scenarios = results.get("scenario_summary", {})
    confidence = results.get("confidence", {})
    evidence = results.get("failure_evidence", [])

    lines: list[str] = []

    # Header
    lines.append("=" * 60)
    lines.append("  RoboGate Benchmark Report")
    lines.append("=" * 60)
    lines.append("")

    # Metadata
    lines.append(f"  Model:      {meta.get('model', 'unknown')}")
    lines.append(f"  Mode:       {meta.get('mode', 'unknown')}")
    lines.append(f"  Scenarios:  {meta.get('total_scenarios', 0)}")
    lines.append(f"  Time:       {meta.get('total_time_s', 0):.1f}s")
    lines.append(f"  Timestamp:  {meta.get('timestamp', '')}")
    lines.append("")

    # Verdict
    verdict = summary.get("verdict", "UNKNOWN")
    score = confidence.get("score", 0)
    sr = summary.get("success_rate", 0)
    verdict_icon = {"PASS": "[PASS]", "WARN": "[WARN]", "FAIL": "[FAIL]"}.get(
        verdict, "[????]"
    )
    lines.append(f"  Verdict:    {verdict_icon}  Confidence: {score}/100")
    lines.append(
        f"  Success:    {summary.get('passed', 0)}/{summary.get('total', 0)} ({sr:.1%})"
    )
    lines.append("")

    # Metrics
    lines.append("-" * 60)
    lines.append("  Metrics")
    lines.append("-" * 60)
    metric_labels = {
        "grasp_success_rate": "Grasp Success Rate",
        "cycle_time": "Avg Cycle Time",
        "collision_count": "Collision Count",
        "drop_rate": "Drop Rate",
        "grasp_miss_rate": "Grasp Miss Rate",
    }
    for mid, label in metric_labels.items():
        val = metrics.get(mid, 0)
        if mid in ("grasp_success_rate", "drop_rate", "grasp_miss_rate"):
            lines.append(f"  {label:25s} {val:.1%}")
        elif mid == "cycle_time":
            lines.append(f"  {label:25s} {val:.2f}s")
        else:
            lines.append(f"  {label:25s} {val}")
    lines.append("")

    # Scenario breakdown
    lines.append("-" * 60)
    lines.append("  Scenario Breakdown")
    lines.append("-" * 60)
    for cat, data in sorted(scenarios.items()):
        p = data.get("passed", 0)
        t = data.get("total", 0)
        rate = data.get("success_rate", 0)
        lines.append(f"  {cat:25s} {p:>3}/{t:<3} ({rate:.1%})")
    lines.append("")

    # Confidence components
    components = confidence.get("components", {})
    if components:
        lines.append("-" * 60)
        lines.append("  Confidence Score Components")
        lines.append("-" * 60)
        weights = confidence.get("weights", {})
        for comp, val in components.items():
            w = weights.get(comp, 0)
            lines.append(f"  {comp:25s} {val:5.1f} (w={w:.2f})")
        lines.append(f"  {'TOTAL':25s} {score:5.1f}/100")
    lines.append("")

    # Failure evidence
    if evidence:
        lines.append("-" * 60)
        lines.append("  Failure Evidence")
        lines.append("-" * 60)
        for e in evidence[:10]:  # Top 10
            lines.append(
                f"  [{e['severity']:>8s}] {e['scenario']:30s} "
                f"{e['failure_type']} x{e['count']}"
            )
    lines.append("")

    lines.append("=" * 60)
    lines.append(f"  Generated by RoboGate Benchmark v{meta.get('version', '1.0.0')}")
    lines.append("=" * 60)

    return "\n".join(lines)


def generate_arena_metrics(results: dict[str, Any]) -> dict[str, float]:
    """Convert results to Isaac Lab-Arena MetricsLogger format.

    Args:
        results: Results dict from evaluation.

    Returns:
        Flat dict of metric_name → float value for MetricsLogger.
    """
    metrics = results.get("metrics", {})
    summary = results.get("summary", {})
    confidence = results.get("confidence", {})

    arena_metrics: dict[str, float] = {
        "robogate/success_rate": metrics.get("grasp_success_rate", 0.0),
        "robogate/cycle_time": metrics.get("cycle_time", 0.0),
        "robogate/collision_count": float(metrics.get("collision_count", 0)),
        "robogate/drop_rate": metrics.get("drop_rate", 0.0),
        "robogate/grasp_miss_rate": metrics.get("grasp_miss_rate", 0.0),
        "robogate/confidence_score": confidence.get("score", 0.0),
        "robogate/total_passed": float(summary.get("passed", 0)),
        "robogate/total_scenarios": float(summary.get("total", 0)),
    }

    # Per-category success rates
    for cat, data in results.get("scenario_summary", {}).items():
        arena_metrics[f"robogate/{cat}/success_rate"] = data.get(
            "success_rate", 0.0
        )

    return arena_metrics
